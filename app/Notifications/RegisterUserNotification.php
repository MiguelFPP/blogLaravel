<?php

namespace App\Notifications;

use App\Models\User;
use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;

class RegisterUserNotification extends Notification
{
    use Queueable;

    private $user;
    private $password;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct(User $user, $password)
    {
        $this->user = $user;
        $this->password = $password;
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return \Illuminate\Notifications\Messages\MailMessage
     */
    public function toMail($notifiable)
    {
        return (new MailMessage)
            ->subject('Registro de Usuario')
            ->line('Hola ' . $this->user->name . ' ' . $this->user->surname)
            ->line('Te damos la bienvenida a la plataforma de administración de contenido de la aplicación.')
            ->line('Tu usuario es: ' . $this->user->email)
            ->line('Tu contraseña es: ' . $this->password)
            ->line('Por favor, cambia tu contraseña una vez inicies sesión.')
            ->line('Ya que has sido registrado desde el panel de administracion, no es necesaria la verificación de tu cuenta.')
            ->action('Ingresar', url('/login'))
            ->line('Gracias por usar nuestra aplicación!');
    }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            //
        ];
    }
}
